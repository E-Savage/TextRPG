using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Engine.Models
{
    // controls all things dealing with the locations (rooms)
    // might need to look into changing the way the rooms work... originally used just an XY Coordinate system, I am going to use XYZ
    // using the Z axis allows for there to be towers and such
    // Z axis is for the vertical axis 
    // X axis and Y axis make up the 2d plane at the base of the world
    // this way the buildings can be as tall as I would like to make them
    public class Location
    {
        public int XCoordinate {  get; set; }
        public int YCoordinate { get; set; }
        public int ZCoordinate { get; set; }    
        public string Name { get; set; }
        public string Description { get; set; }
        public string ImageName { get; set; }
    }
}
